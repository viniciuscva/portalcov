<h1>Sobre a Covid-19</h1>
<br>

<h2> O que é a Covid-19?</h2>
<br>
Conforme o texto publicado no
 <a href="https://www.gov.br/saude/pt-br/coronavirus/o-que-e-o-coronavirus">
 Site do Ministério da Saúde</a>, temos a seguinte definição:
 <br><br>

<div class='citacao'>
    <p>
    A Covid-19 é uma infecção respiratória aguda causada pelo coronavírus SARS-CoV-2, 
    potencialmente grave, de elevada transmissibilidade e de distribuição global.
    </p>
    <p>
    O SARS-CoV-2 é um betacoronavírus descoberto em amostras de lavado broncoalveolar 
    obtidas de pacientes com pneumonia de causa desconhecida na cidade de Wuhan, província
    de Hubei, China, em dezembro de 2019. Pertence ao subgênero Sarbecovírus da família 
    Coronaviridae e é o sétimo coronavírus conhecido a infectar seres humanos.
    </p>
    <p>
    Os coronavírus são uma grande família de vírus comuns em muitas espécies diferentes de
    animais, incluindo o homem, camelos, gado, gatos e morcegos. Raramente os coronavírus 
    de animais podem infectar pessoas e depois se espalhar entre seres humanos como já ocorreu 
    com o MERS-CoV e o SARS-CoV-2. Até o momento, não foi definido o reservatório silvestre 
    do SARS-CoV-2.
    </p>
</div>

